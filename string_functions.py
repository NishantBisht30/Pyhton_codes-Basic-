mystr = "Hello i'm here"

print(len(mystr))  # gives length of string
print(mystr.upper()) # lower to upper
print(mystr.lower())    # upper to lower
print(mystr[3:])     # string slicing
print(mystr.count("z"))          # count the number of occurance
print(mystr.isalnum())      # The isalnum() method returns True if all characters in the string are alphanumeric (either alphabets or numbers).
print(mystr.replace("Hello","Ello"))  # replace the string
